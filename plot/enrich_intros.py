def main():
    mod = load_expand_module()
    missing = [c["id"] for c in mod.RICH if c["id"] not in INTROS]
    if missing:
        raise SystemExit(f"缺少介绍: {missing}")

    for c in mod.RICH:
        c["intro"] = INTROS[c["id"]]

    chars = []
    for c in mod.RICH:
        chars.append(
            {
                "id": c["id"],
                "name": c["name"],
                "aliases": c["aliases"],
                "work": c["work"],
                "year": c["year"],
                "age_note": c["age_note"],
                "appearance": c["appearance"],
                "blurb": c["blurb"],  # 仅封面卡片短标签备用，正文以 intro 为准
                "intro": c["intro"],
                "work_intro": c["work_intro"],
                "background": c["background"],
                "filth_seed": c["filth_seed"],
                "accent": c["accent"],
            }
        )

    yaml_path = ROOT / "plot/characters.yaml"
    yaml_path.write_text(
        yaml.safe_dump({"characters": chars}, allow_unicode=True, sort_keys=False),
        encoding="utf-8",
    )

    out = ROOT / "worldbook/角色"
    out.mkdir(parents=True, exist_ok=True)
    for p in out.glob("*.md"):
        p.unlink()
    for c in chars:
        (out / f"{c['name']}.md").write_text(write_md(c), encoding="utf-8")

    # review doc
    lines = [
        "# 圣堂初遇 · 角色介绍文案审查（角色池）",
        "",
        "> 下列为每人详细「角色介绍」，请直接批注修改。",
        "",
    ]
    for i, c in enumerate(chars, 1):
        lines += [
            f"## {i}. {c['name']}（{c['work']} · {c['year']}）",
            "",
            "### 角色介绍",
            c["intro"],
            "",
            f"- 外貌：{c['appearance']}",
            f"- 作品简介：{c['work_intro']}",
            "- 设定要点：",
        ]
        for b in c["background"]:
            lines.append(f"  - {b}")
        lines += [f"- 污秽种子：{c['filth_seed']}", ""]

    review = ROOT / "plot/文案审查_角色池50.md"
    review.write_text("\n".join(lines), encoding="utf-8")
    old = ROOT / "plot/文案审查_角色池40.md"
    if old.exists():
        old.unlink()
    print(f"updated {len(chars)} intros -> {yaml_path.name}, worldbook, {review.name}")


if __name__ == "__main__":
    main()
