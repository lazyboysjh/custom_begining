def to_yaml_chars():
    out = []
    for c in RICH:
        out.append(
            {
                "id": c["id"],
                "name": c["name"],
                "aliases": c["aliases"],
                "work": c["work"],
                "year": c["year"],
                "age_note": c["age_note"],
                "appearance": c["appearance"],
                "blurb": c["blurb"],
                "work_intro": c["work_intro"],
                "background": c["background"],
                "filth_seed": c["filth_seed"],
                "accent": c["accent"],
            }
        )
    return out


def write_md(c: dict) -> str:
    aliases = "、".join(str(a) for a in c["aliases"])
    bg = "\n".join(f"    - {x}" for x in c["background"])
    return f"""角色档案:
  基本信息:
    姓名: {c['name']}
    别名: {aliases}
    作品: {c['work']}（约 {c['year']} 起热度）
    年龄: {c['age_note']}
    身份: 原作身份见背景；本卡开局为初遇对象
    与{{{{user}}}}关系: 开局陌路/求助，以变量「初遇.与user关系」为准

  外貌特征:
    - {c['appearance']}

  背景设定:
    作品简介: {c['work_intro']}
    角色要点:
{bg}
    污秽种子: {c['filth_seed']}

  关系设定:
    - 与{{{{user}}}}无旧识；互动从封面相遇方式开始
    - 对净化可戒备、配合或半信半疑；随信任/好感/堕落变化
    - 保留原作口吻与辨识反应，不写成万能美人模板
"""


def main():
    chars = to_yaml_chars()
    yaml_path = ROOT / "plot/characters.yaml"
    yaml_path.write_text(
        yaml.safe_dump({"characters": chars}, allow_unicode=True, sort_keys=False),
        encoding="utf-8",
    )
    out = ROOT / "worldbook/角色"
    out.mkdir(parents=True, exist_ok=True)
    # clear old md
    for p in out.glob("*.md"):
        p.unlink()
    for c in RICH:
        (out / f"{c['name']}.md").write_text(write_md(c), encoding="utf-8")
    print(f"wrote {len(RICH)} profiles + {yaml_path}")


if __name__ == "__main__":
    main()
